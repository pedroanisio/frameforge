def _write(rel, text):
    path = os.path.join(DOCS, rel)
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as fh:
        fh.write(text)
    return rel


def generate():
    os.makedirs(DOCS, exist_ok=True)
    written = [
        _write("reference.md", gen_reference()),
        _write("grammar.md", gen_grammar()),
        _write("spec.md", _verbatim("docs/spec/frameforge-v2-spec.md", "the normative reference")),
        _write("sdk.md", gen_sdk_guide()),
        _write("sdk-api.md", gen_sdk_api()),
        _write("changelog.md", _verbatim("CHANGELOG.md", "version history")),
        _write("fixtures.md", gen_fixtures()),
    ]
    return written


# Generated pages that are *committed* (not gitignored): the SDK guide/API are
# checked-in snapshots, so `--check` must fail if the committed copy drifts from a
# fresh build — the same "generate + --check, never hand-drift" contract that
# build_schema.py --check gives the schema. The transient pages are gitignored and
# need no such gate.
_TRACKED_GENERATED = {"sdk.md": gen_sdk_guide, "sdk-api.md": gen_sdk_api}


def stale_tracked_pages():
    """Committed generated pages whose on-disk content differs from a fresh build.
    Read this BEFORE generate() runs, since generate() overwrites them in place."""
    stale = []
    for rel, builder in _TRACKED_GENERATED.items():
        path = os.path.join(DOCS, rel)
        on_disk = open(path, encoding="utf-8").read() if os.path.exists(path) else ""
        if on_disk != builder():
            stale.append(rel)
    return stale


def write_tracked():
    """Regenerate only the *committed* snapshots (``sdk.md``/``sdk-api.md``).

    The cheap fix when ``--check`` reports SDK-snapshot drift: it skips the
    fixture-gallery render the full :func:`generate` does (41 docs → SVG), so
    refreshing the snapshot after an SDK change costs a fraction of `make docs`."""
    return [_write(rel, builder()) for rel, builder in _TRACKED_GENERATED.items()]


def _nav_targets():
    import yaml
    cfg = yaml.safe_load(open(os.path.join(ROOT, "mkdocs.yml"), encoding="utf-8"))
    return list(_nav_entry_targets(cfg.get("nav", [])))


def _nav_entry_targets(entries):
    for entry in entries:
        if isinstance(entry, str):
            yield entry
        elif isinstance(entry, dict):
            for value in entry.values():
                if isinstance(value, str):
                    yield value
                elif isinstance(value, list):
                    yield from _nav_entry_targets(value)


def main(argv=None):
    ap = argparse.ArgumentParser(description=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--check", action="store_true",
                    help="generate, then assert committed generated pages are fresh "
                         "and every mkdocs.yml nav page exists")
    ap.add_argument("--sdk", action="store_true",
                    help="regenerate ONLY the committed SDK snapshots (sdk.md, "
                         "sdk-api.md) — fast; the cheap fix when --check reports drift")
    args = ap.parse_args(argv)

    if args.sdk:
        written = write_tracked()
        print(f"Wrote {len(written)} SDK snapshot(s) to docs/: {', '.join(written)}")
        return 0

    # Capture drift in the committed snapshots BEFORE generate() overwrites them.
    stale = stale_tracked_pages() if args.check else []

    written = generate()
    print(f"Wrote {len(written)} page(s) to docs/: {', '.join(written)}")

    if args.check:
        if stale:
            print(f"STALE: committed generated docs differ from a fresh build: {stale}. "
                  f"Run `make docs-sdk` (fast) and commit the result.")
            return 1
        missing = [t for t in _nav_targets() if not os.path.exists(os.path.join(DOCS, t))]
        if missing:
            print(f"NAV BROKEN: mkdocs.yml references missing pages: {missing}")
            return 1
        print("OK: committed generated docs are fresh and every mkdocs.yml nav page exists.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
