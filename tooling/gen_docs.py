def _anchor(name):
    return name.lower().replace("_", "-")


def _type_str(s):
    """A compact human type summary for a JSON-Schema property node."""
    if not isinstance(s, dict):
        return "any"
    if "$ref" in s:
        n = s["$ref"].split("/")[-1]
        return f"[`{n}`](#{_anchor(n)})"
    if "const" in s:
        return f"`{s['const']!r}`"
    if "enum" in s:
        vals = [f"`{v}`" for v in s["enum"]]
        return " \\| ".join(vals[:10]) + (" …" if len(vals) > 10 else "")
    if "anyOf" in s or "oneOf" in s:
        parts = [_type_str(x) for x in s.get("anyOf", s.get("oneOf", [])) if x.get("type") != "null"]
        seen = list(dict.fromkeys(parts))
        return " \\| ".join(seen) if seen else "null"
    t = s.get("type")
    if t == "array":
        return f"array&lt;{_type_str(s.get('items', {}))}&gt;"
    if isinstance(t, list):
        return " \\| ".join(t)
    if t:
        return f"{t} ({s['format']})" if s.get("format") else t
    return "object" if "properties" in s else "any"


def _model_section(name, defn):
    out = [f"## `{name}`", ""]
    if defn.get("description"):
        out += [defn["description"].strip(), ""]
    props = defn.get("properties")
    if not props:
        if "enum" in defn:
            out += ["One of: " + ", ".join(f"`{v}`" for v in defn["enum"]), ""]
        return "\n".join(out) + "\n"
    required = set(defn.get("required", []))
    out += ["| Property | Type | Required | Description |", "|---|---|:--:|---|"]
    for pname, pschema in props.items():
        req = "✓" if pname in required else ""
        desc = (pschema.get("description", "") or "").replace("\n", " ").replace("|", "\\|")
        out.append(f"| `{pname}` | {_type_str(pschema)} | {req} | {desc} |")
    out.append("")
    return "\n".join(out) + "\n"


def gen_reference():
    schema = B.build()
    defs = schema.get("$defs", {})
    lines = [
        "# Schema reference",
        "",
        "!!! info \"Generated\"",
        f"    Generated by `tooling/gen_docs.py` from `schema/frameforge-v2.schema.json` "
        f"— **{len(defs)} models** ({schema.get('title','').split('—')[0].strip()}). "
        f"Do not hand-edit. The schema itself is generated from the Pydantic models.",
        "",
    ]
    # Document is the schema root, not a $defs member. Render it first so the
    # reference covers the full public model surface, then list $defs alphabetically.
    order = ["Document"] + sorted(defs)
    lines.append("**Models:** " + " · ".join(f"[`{k}`](#{_anchor(k)})" for k in order))
    lines.append("")
    for name in order:
        lines.append(_model_section(name, schema if name == "Document" else defs[name]))
    return "\n".join(lines) + "\n"


