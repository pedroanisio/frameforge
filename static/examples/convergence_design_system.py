def arrow(L, x1, y1, x2, y2, color=BONE300, w=2.0, head=7.0):
    L.line([x1, y1], [x2, y2], **stroke(w, color=color, cap="round"))
    ang = math.atan2(y2 - y1, x2 - x1)
    for da in (2.65, -2.65):
        L.line([x2, y2], [x2 + head * math.cos(ang + da),
                          y2 + head * math.sin(ang + da)],
               **stroke(w, color=color, cap="round"))


def arc(L, cx, cy, r, a0, a1, color=BONE100, w=3.0, n=48, dash=None):
    pts = []
    for k in range(n + 1):
        a = math.radians(a0 + (a1 - a0) * k / n)
        pts.append([cx + r * math.cos(a), cy + r * math.sin(a)])
    L.polyline(pts, **stroke(w, color=color, cap="round", dash=dash))


