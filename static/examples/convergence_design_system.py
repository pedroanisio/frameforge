    feeds = [(148, CYAN), (180, VIOLET), (212, AMBER), (116, MINT),
             (244, SLATE)]
    for a, c in feeds:
        th = math.radians(a)
        x0, y0 = cx + r * 1.85 * math.cos(th), cy + r * 1.85 * math.sin(th)
        x1, y1 = cx + r * 1.06 * math.cos(th), cy + r * 1.06 * math.sin(th)
        L.line([x0, y0], [x1, y1], **stroke(lw, color=c, cap="round"))
        L.circle([x0, y0], lw * 1.35, fill=c)
    ex = math.radians(0)
    x0 = cx + r * math.cos(math.radians(20))
    y0 = cy + r * math.sin(math.radians(-0)) * 0
    arrow(L, cx + r * 0.9, cy, cx + r + exit_len, cy, color=CORAL, w=lw,
          head=lw * 3.2)


