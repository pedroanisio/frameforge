def T(L, box, s, size=15.5, color=BONE300, weight=400, family=UI,
      align="left", lh=1.5, ls=None, opacity=None):
    st = {"font_family": family, "size": size, "color": color,
          "weight": weight, "align": align, "line_height": lh}
    if ls is not None:
        st["letter_spacing"] = ls
    if isinstance(s, str) and "\n" in s:
        st["white_space"] = "pre-line"
    kw = {}
    if opacity is not None:
        kw["opacity"] = opacity
    L.text(box, s, style=st, **kw)


