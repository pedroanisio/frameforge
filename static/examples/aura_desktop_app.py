def scene(x, y, w, h, grad, kind, radius=14, lay=None):
    """A photo-like tile: gradient ground + a tiny vector scene, clipped feel."""
    L = lay or D
    L.rect([x, y, w, h], fill=grad, radius=radius)
    cxm = x + w / 2
    if kind == "sky":
        L.circle([x + w * 0.74, y + h * 0.3], min(w, h) * 0.14, fill="#FFFFFF", fill_opacity=0.85)
        L.path([("M", x, y + h), ("L", x + w * 0.34, y + h * 0.52), ("L", x + w * 0.58, y + h * 0.74),
                ("L", x + w * 0.8, y + h * 0.5), ("L", x + w, y + h * 0.72), ("L", x + w, y + h), ("Z",)],
               fill="#FFFFFF", fill_opacity=0.22, stroke="none")
    elif kind == "city":
        for i, (bx, bh) in enumerate([(0.1, 0.4), (0.26, 0.62), (0.44, 0.32), (0.6, 0.55), (0.78, 0.44)]):
            L.rect([x + w * bx, y + h * (1 - bh), w * 0.13, h * bh], fill="#FFFFFF", fill_opacity=0.2, radius=1)
    elif kind == "forest":
        for bx in (0.22, 0.44, 0.66):
            L.path([("M", x + w * bx, y + h * 0.85), ("L", x + w * (bx + 0.09), y + h * 0.4),
                    ("L", x + w * (bx + 0.18), y + h * 0.85), ("Z",)], fill="#FFFFFF", fill_opacity=0.24, stroke="none")
    elif kind == "gold":
        L.circle([cxm, y + h * 0.42], min(w, h) * 0.2, fill="#FFFFFF", fill_opacity=0.32)
        L.rect([x, y + h * 0.66, w, h * 0.34], fill="#000000", fill_opacity=0.08)
