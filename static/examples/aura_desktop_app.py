"""Aura — a polished DESKTOP web app, authored with the FrameForge SDK.

Design brief: a calm desktop hub for your music, photos, and the people who
share them — recomposed for a 1512×964 desktop surface from a set of iOS
reference screens (Focus modes, "Shared with You", Listen Now, Photos search,
Messages). Every reference element reappears here as a native desktop control:

    · a persistent left navigation rail (deep-indigo glass)
    · a global AI search + focus/presence controls in the top bar
    · "Shared with You" media cards, a Photos-from-apps strip, Recently Played
    · a right rail: the Focus module (dark glass, like the reference), Messages,
      and the People sharing with you
    · a full-width Now-Playing bar

Eight surfaces ship — the same product flow the FrameForge Studio wireframe
(static/examples/build_wireframe_annotated.py) maps out, recomposed for Aura:

    1. Home        — the shared feed (primary surface)
    2. Photos      — search results across your apps
    3. Sign in     — split brand / auth panel        (≈ wireframe "Sign in")
    4. Sync run    — connect → scan → match pipeline  (≈ wireframe "Render run")
    5. Activity    — timeline + detail                (≈ wireframe "History")
    6. Viewer      — a public, read-only shared album (≈ wireframe "Viewer")
    7. Settings    — profile · connected apps · privacy (≈ wireframe "Settings")
    8. Sitemap     — the information architecture + journeys (≈ wireframe "Sitemap")

This is a pure FrameForge composition — vectors only, no external assets — so it
renders identically in the raster feedback loop and to SVG/PDF.

Provenance: generated by Claude Opus 4.8 via Claude Code (FrameForge SDK 2.4.1),
2026-07-03. UI concept and layout are original; the reference screens are an
Apple-ecosystem UX vocabulary (Focus, Shared with You, Listen Now, Photos).
"""
