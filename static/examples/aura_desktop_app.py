def T(x, y, w, s, *, size=13, color=INK, weight=None, align=None, spacing=None,
      caps=False, mono=False, valign="top", italic=False, h=None, lay=None):
    L = lay or D
    st = {"font_family": list(TH.mono if mono else TH.font), "font_size": size,
          "color": color, "vertical_align": valign}
    if weight is not None: st["font_weight"] = weight
    if align: st["text_align"] = align
    if spacing is not None: st["letter_spacing"] = spacing
    if caps: st["text_transform"] = "uppercase"
    if italic: st["font_style"] = "italic"
    L.text([x, y, w, h if h is not None else size * 1.4], s, style=st)

def soft(box, *, fill=CARD, radius=16, stroke=None, sw=1.0, blur=22, dy=6,
         sh="#141A2610", lay=None):
    L = lay or D
    f = {"fill": fill, "radius": radius}
    if stroke: f["stroke"] = stroke; f["stroke_style"] = {"stroke_width": sw}
    if blur: f["shadow"] = {"dx": 0, "dy": dy, "blur": blur, "color": sh}
    L.rect(box, **f)

