        L.circle([cxm, y + h / 2], min(w, h) * 0.34, fill="#FFFFFF", fill_opacity=0.18)
        L.circle([cxm, y + h / 2], min(w, h) * 0.1, fill="#FFFFFF", fill_opacity=0.5)

