def _appendix_a(bk: Tour):
    bk.section("Appendix A", f"All {len(BY_KIND['sdk_export'])} SDK "
               "exports, introspected", "Appendix A")
    bk.callout("GENERATED MACHINE REFERENCE — built by importing "
               "framegraph.sdk and reading each export's docstring at build "
               "time, so it cannot drift from the code; the coverage gate "
               "fails if this surface and the manifest disagree. Not meant "
               "to be read linearly: scan by module, or arrive from "
               "Appendix C. The human-guide view of the same material is "
               "§1–§18. The machine-readable original ships beside the "
               "repo as docs/capability-manifest.json.",
               accent=ACCENT_MID, size=10)
    groups: dict[str, list[tuple[str, str]]] = {}
    for name in BY_KIND["sdk_export"]:
        obj = getattr(sdk, name)
        if isinstance(obj, str):
            mod, one = "sdk.model", f'the version constant ("{obj}")'
        elif getattr(obj, "__class__", None) is not None and obj.__class__.__name__ == "module":
            mod = "modules"
            one = ((obj.__doc__ or "").strip().splitlines() or [""])[0]
        else:
            mod = (getattr(obj, "__module__", "") or "").replace(
                "framegraph.sdk.", "sdk.").replace("framegraph.", "")
            if mod.startswith("pydantic_core"):
                mod = "sdk.model (re-exported)"
            if mod in ("typing",):
                one = "type alias"
            else:
                one = ((getattr(obj, "__doc__", "") or "").strip().splitlines()
                       or [""])[0]
        one = one.replace("``", "'").replace(":class:", "").replace(
            ":func:", "").replace(":meth:", "")
        if one and not one.rstrip().endswith((".", ":", ")")):
            # first LINE can cut mid-sentence; extend to the first sentence
            # of the docstring's opening paragraph instead (review FGTOUR2-015)
            doc_all = (getattr(obj, "__doc__", "") or "").strip()
            para0 = " ".join(doc_all.split("\n\n")[0].split())
            para0 = para0.replace("``", "'").replace(":class:", "").replace(
                ":func:", "").replace(":meth:", "")
