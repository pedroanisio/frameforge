def coverage_gate(doc) -> list[str]:
    """Every manifest capability name must appear in the serialized document."""
    blob = serialize(doc, format="json")   # JSON: strings never line-wrap
    return [c["name"] for c in MANIFEST["capabilities"] if c["name"] not in blob]


def main() -> int:
    b = build()
    doc = b.build()
    report = validate_static_rules(doc)
    errors = [i for i in report.issues if i.severity == "error"]
    warns = [i for i in report.issues if i.severity != "error"]
    print(f"Built {len(doc.pages)} section(s)/page(s) — ok={report.ok} "
          f"errors={len(errors)} warnings={len(warns)}")
    for i in report.issues[:40]:
        print(f"  [{i.severity}] [{i.rule_id}] {i.path}: {i.message}")

    out_dir = os.path.join(ROOT, "_tmp", "capability-tour")
    os.makedirs(out_dir, exist_ok=True)
    out_yaml = os.path.join(out_dir, "capability-tour.fg.yaml")
    with open(out_yaml, "w", encoding="utf-8") as fh:
        fh.write(serialize(doc, format="yaml"))
    print(f"Wrote {out_yaml}")

    # io round-trip: canonical text -> model -> identical plain dict.
    doc2 = parse(serialize(doc, format="yaml"))
    assert to_plain_dict(doc2) == to_plain_dict(doc), "serialize/parse drift"
    print("Round-trip serialize→parse→to_plain_dict: identical")

    svgs, stats = render_pages_with_stats(doc)
    for idx, svg in enumerate(svgs, 1):
        with open(os.path.join(out_dir, f"page-{idx:02d}.svg"), "w",
                  encoding="utf-8") as fh:
            fh.write(svg)
    print(f"Wrote {len(svgs)} page SVG(s) to {out_dir} — stats: {stats}")
    fit_ok = (stats.get("visible_overflow", 0) == 0
              and stats.get("uncontained", 0) == 0)
    print(f"GATE text-fit: visible_overflow={stats.get('visible_overflow')} "
          f"uncontained={stats.get('uncontained')} -> "
          f"{'PASS' if fit_ok else 'FAIL'}")

    # Text-artifact audit (review FGTOUR2-002): soft hyphens, replacement
    # glyphs, zero-width and BOM characters must never reach the document.
    blob = serialize(doc, format="json")
    artifacts = {"­": "SOFT-HYPHEN", "�": "REPLACEMENT",
                 "￾": "U+FFFE", "​": "ZWSP", "﻿": "BOM"}
    found = {label: blob.count(ch) for ch, label in artifacts.items()
             if ch in blob}
    print(f"GATE text-artifacts: {found if found else 'none'} -> "
          f"{'FAIL' if found else 'PASS'}")

    hashes = page_hashes(doc)
    write_golden(os.path.join(out_dir, "golden.txt"), hashes)
    assert_golden(doc, hashes)
    print(f"Golden: {len(hashes)} page hashes pinned + asserted")

    missing = coverage_gate(doc)
    if missing:
        print(f"COVERAGE GATE FAILED — {len(missing)} capability name(s) "
              f"absent: {', '.join(missing[:12])}"
              + (" …" if len(missing) > 12 else ""))
    else:
        print(f"Coverage gate: all {len(MANIFEST['capabilities'])} manifest "
              f"capability names present in the serialized document")
    return 1 if (errors or missing or not fit_ok) else 0


if __name__ == "__main__":
    raise SystemExit(main())
