def _clip_motif(lp, w, h):
    lp.rect([0, 0, w, h], fill="#DCE7F5")
    lp.circle([w * 0.30, h * 0.40], 20, fill=ACCENT)
    lp.circle([w * 0.72, h * 0.68], 26, fill="#D4A017")
    lp.rect([w * 0.12, h * 0.56, w * 0.6, 9], fill=INK)


def _clip_cell(clip_of):
    # Clip shapes are evaluated in PAGE coordinates (or derived from the box,
    # like clip_inset / the no-arg forms) — children of a local() group stay
    # local, the clip does not. Learned from the pixels, kept as a lesson.
    def draw(pg, bx):
        x, y, w, h = bx
        with pg.local([x, y, w, h], clip=clip_of(x, y, w, h)) as lp:
            _clip_motif(lp, w, h)
    return draw


