# The book's serif is Charter; on hosts without the "Charter" family a fallback
# rasterizer (cairosvg + fontconfig) substitutes a wider face and exactly-full
# lines lose their tail word to the box clip. The tour therefore wraps its serif
# prose a little narrower than the elected composer does, so the pages survive
# every raster runtime — docker/chromium remains the canonical one. 0.90 covers
# the widest observed substitute (Noto Sans, ~9% wider than Charter).
SLACK = 0.90


class Tour(Book):
    """``layout_methods_book.Book`` extended for the capability tour."""

    def __init__(self, title: str, subtitle: str):
        super().__init__(title, subtitle)
        # page-mode chapters + flow-mode sections in one document
        self.b = DocumentBuilder(title=title, profile="mixed", lang="en")
        self._contents: list[tuple[str, str, int]] = []   # (num, title, folio)

    def section(self, num, title, running, *, keep=96):
        super().section(num, title, running, keep=keep)
        self._contents.append((num, title, self.pno))

    # -- slack overrides: the elected emitters, narrower author-side wrap --- #
    def para(self, text, *, size=BODY_SIZE, color=INK, lh=BODY_LH, gap=10,
             indent=0):
        if self.page is None:
            self.new_page()
        base = ts(size, color)
        step = size * lh
        lines = wrap_atoms(text, (CONTENT_W - indent) * SLACK, size)
        for line in lines:
            if self.y + step > BOTTOM:
                self.new_page()
            x = MX + indent
            spans = line_spans(line, size, color)
            if spans:
                self.page.add({"type": "text",
                               "box": [x, self.y, CONTENT_W - indent, step],
                               "spans": spans, "style": base})
            self.y += step
        self.y += gap

    def bullets(self, items, *, size=BODY_SIZE, lh=BODY_LH, gap=10,
                ordered=False):
        step = size * lh
        marker_w = 26
        for k, item in enumerate(items):
            mark = f"{k + 1}." if ordered else "•"
            lines = wrap_atoms(item, (CONTENT_W - marker_w) * SLACK, size)
            for li, line in enumerate(lines):
                if self.y + step > BOTTOM:
                    self.new_page()
                if li == 0:
                    self.page.text([MX, self.y, marker_w, step], mark,
                                   style=ts(size, ACCENT_MID, family=SANS,
                                            weight=700 if ordered else 400,
                                            align="left"))
                spans = line_spans(line, size, INK)
                if spans:
                    self.page.add({"type": "text",
                                   "box": [MX + marker_w, self.y,
                                           CONTENT_W - marker_w, step],
                                   "spans": spans, "style": ts(size, INK)})
                self.y += step
            self.y += 4
        self.y += gap - 4

    def callout(self, text, *, accent=ACCENT, fill=PANEL, size=11, mono=False):
        fam = MONO if mono else SERIF
        inner_w = CONTENT_W - 28 - 18
        lines = sdk.wrap_text(text, width=inner_w * SLACK, font_family=fam,
                              font_size=size)
        step = size * 1.5
        box_h = step * len(lines) + 24
        self.ensure(box_h + 12)
        pg = self.page
        y0 = self.y
        pg.rect([MX, y0, CONTENT_W, box_h], radius=7, fill=fill)
        pg.rect([MX, y0, 4, box_h], radius=2, fill=accent)
        ty = y0 + 12
        for ln in lines:
            pg.text([MX + 22, ty, inner_w, step], ln,
                    style=ts(size, INK, family=fam, lh=1.5,
                             style="italic" if not mono else None))
            ty += step
        self.y = y0 + box_h + 14

    def cover(self, *, kicker, title, subtitle, author, date, note, epigraph):
        self.new_page(chrome=False, folio=False)
        pg = self.page
        pg.rect([0, 0, 816, 8], fill=ACCENT)
        y = 250
        pg.rect([MX, y, 46, 3.4], fill=ACCENT)
        pg.text([MX, y + 16, CONTENT_W, 16], kicker,
                style=ts(11, ACCENT_MID, family=SANS, weight=800, spacing=3.0,
                         transform="uppercase"))
        y += 52
        for ln in sdk.wrap_text(title, width=CONTENT_W, font_family=SANS,
                                font_size=46, bold=True):
            pg.text([MX, y, CONTENT_W, 58], ln,
                    style=ts(46, INK, family=SANS, weight=800, spacing=-1.2))
            y += 56
        y += 8
        for ln in sdk.wrap_text(subtitle, width=(CONTENT_W - 40) * SLACK,
                                font_family=SERIF, font_size=17):
            pg.text([MX, y, CONTENT_W - 40, 24], ln,
                    style=ts(17, MUTE, family=SERIF, style="italic", lh=1.4))
            y += 25
        y += 26
        pg.rect([MX, y, CONTENT_W, 1.0], fill=RULE)
        y += 16
        pg.text([MX, y, CONTENT_W, 16], author,
                style=ts(11.5, INK, family=SANS, weight=700))
        pg.text([MX, y, CONTENT_W, 16], date,
                style=ts(11.5, MUTE, family=SANS, align="right"))
        y += 40
        for ln in sdk.wrap_text(note, width=CONTENT_W * SLACK,
                                font_family=SERIF, font_size=11):
            pg.text([MX, y, CONTENT_W, 16], ln,
                    style=ts(11, MUTE, family=SERIF, lh=1.5))
            y += 17
        # Epigraph, deliberately quieter than the subtitle/provenance above
        # (review FGTOUR-021): muted ink, smaller size, hairline accent.
        ey = 896
        pg.rect([MX, ey, 3, 40], radius=1.5, fill=ACCENT_MID)
        for ln in sdk.wrap_text(epigraph, width=(CONTENT_W - 30) * SLACK,
                                font_family=SERIF, font_size=11.5):
            pg.text([MX + 20, ey, CONTENT_W - 30, 18], ln,
                    style=ts(11.5, MUTE, family=SERIF, style="italic", lh=1.5))
            ey += 18
        self.y = BOTTOM + 1

    def gallery(self, cells, *, cols=3, cell_h=118, gap=14):
        """A grid of framed demo cells; each cell = (label, draw(pg, box))."""
        label_h = 15
        for start in range(0, len(cells), cols):
            chunk = cells[start:start + cols]
            self.ensure(cell_h + label_h + gap)
            pg = self.page
            boxes = row([MX, self.y, CONTENT_W, cell_h], cols, gap=gap)
            for (label, draw), bx in zip(chunk, boxes):
                pg.rect(bx, radius=8, fill="#FFFFFF", stroke=LINE,
                        stroke_style={"stroke_width": 1.0})
                draw(pg, inset(bx, 10))
                pg.text([bx[0] + 4, self.y + cell_h + 4, bx[2] - 8, 11], label,
                        style=ts(8, MUTE, family=MONO))
            self.y += cell_h + label_h + gap
        self.y += 4

    def verify(self, text):
        """The PALS's-Law closing move of every section: what to check on pixels."""
        self.callout("VERIFY — " + text, accent=ACCENT_MID, size=10)

    def ref_table(self, headers, rows, *, weights=None, chunk=22, row_height=22):
        """A long generated table, chunked so every slice fits one page."""
        for i in range(0, len(rows), chunk):
            self.table(headers, rows[i:i + chunk], weights=weights,
                       row_height=row_height, header_height=28)

    def ref_columns(self, entries, *, size=8.5, gap=22):
        """Two-column reference flow for scannable machine-reference matter:
        ('head', text) group headings and ('entry', name, summary) items fill
        the left column to the page bottom, then the right, then a fresh
        page. Entries hang — first line on the column edge, continuation
        lines indented — per the design-review guidance."""
        colw = (CONTENT_W - gap) / 2
        step = size * 1.35
        hang = 10
        col = 0
        top = self.y

        def advance(needed):
            nonlocal col, top
            if self.y + needed > BOTTOM:
                if col == 0:
                    col = 1
                else:
                    self.new_page()
                    col = 0
                    top = self.y
                self.y = top

        for item in entries:
            x0 = MX + col * (colw + gap)
            if item[0] == "head":
                advance(step + 12 + 2 * step)   # keep the head with two lines
                x0 = MX + col * (colw + gap)
                self.page.text([x0, self.y + 6, colw, step + 3], item[1],
                               style=ts(10, ACCENT, family=SANS, weight=800))
                self.y += step + 12
                continue
            _, name, one = item
            text = f"`{name}` — {one}" if one else f"`{name}`"
            lines = wrap_atoms(text, (colw - hang) * SLACK, size)
            advance(len(lines) * step + 2)
            x0 = MX + col * (colw + gap)
            for li, line in enumerate(lines):
                spans = line_spans(line, size, INK)
                lx = x0 + (hang if li else 0)
                if spans:
                    self.page.add({"type": "text",
                                   "box": [lx, self.y, colw - (hang if li else 0),
                                           step],
                                   "spans": spans, "style": ts(size, INK)})
                self.y += step
            self.y += 2
        self.y = BOTTOM + 1        # nothing flows beside a half-filled column


