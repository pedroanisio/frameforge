def _s11(bk: Tour):
    bk.section("§ 11", "Graphs — nodes, edges, five layouts", "§11 · Graphs")
    bk.para("`Graph` is a node-link model with deterministic layouts and a "
            "single-group renderer. `node(id, label, weight, pos)` pins or "
            "weights vertices; `edge(src, dst, directed=True)` adds an "
            "arrowhead; layouts return positions you pass to `render`. "
            "`auto_layout` picks by structure — `layout_kind()` names the "
            "choice: tree -> radial, DAG -> layered, dense -> circular, "
            "otherwise spring.")
    bk.code(
        'g = Graph()\n'
        'g.node("a", "API", weight=1.4).node("b", "model")\n'
        'g.edge("a", "b", directed=True, label="lowers")\n'
        'pos = g.circular_layout() | g.grid_layout() | g.layered_layout()\n'
        '    | g.radial_layout(root) | g.spring_layout(seed_radius=1.0)\n'
        'page.add(g.render(pos, box=box, node_radius=7, label_size=7))\n'
        'pos = g.auto_layout()        # picks via g.layout_kind()'
    )

    def _ring():
        g = Graph()
        for i in range(6):
            g.node(f"r{i}", f"n{i}")
        for i in range(6):
            g.edge(f"r{i}", f"r{(i + 1) % 6}")
        return g

    def d_circ(pg, bx):
        g = _ring()
        pg.add(g.render(g.circular_layout(), box=bx, node_radius=6,
                        node_fill=ACCENT, label_size=6.5))

    def d_grid(pg, bx):
        g = Graph()
        for i in range(9):
            g.node(f"g{i}", str(i))
        pg.add(g.render(g.grid_layout(cols=3), box=bx, node_radius=6,
                        node_fill="#33415F", label_size=6.5))

    def d_layer(pg, bx):
        g = Graph()
        for n in ("src", "a", "b", "c", "out"):
            g.node(n, n)
        for s, d in (("src", "a"), ("src", "b"), ("a", "c"), ("b", "c"),
                     ("c", "out")):
            g.edge(s, d, directed=True)
        pg.add(g.render(g.layered_layout(), box=bx, node_radius=6,
                        node_fill="#B45309", label_size=6.5))

    def d_radial(pg, bx):
        g = Graph()
        g.node("root", "R", weight=1.5)
        for i in range(3):
            g.node(f"c{i}", f"c{i}")
            g.edge("root", f"c{i}")
            for j in range(2):
                g.node(f"c{i}{j}", "")
                g.edge(f"c{i}", f"c{i}{j}")
        pg.add(g.render(g.radial_layout("root"), box=bx, node_radius=5,
                        node_fill="#166534", label_size=6.5))

    def d_spring(pg, bx):
        g = Graph()
        for i in range(7):
            g.node(f"s{i}", "")
        for s, d in (("s0", "s1"), ("s0", "s2"), ("s1", "s2"), ("s2", "s3"),
                     ("s3", "s4"), ("s4", "s5"), ("s4", "s6"), ("s5", "s6")):
            g.edge(s, d)
        pg.add(g.render(g.spring_layout(iterations=180), box=bx,
                        node_radius=5, node_fill="#6D28D9", labels=False))

    ga = Graph()
    for n in ("in", "x", "y", "z"):
        ga.node(n, n)
    for s, d in (("in", "x"), ("x", "y"), ("x", "z")):
        ga.edge(s, d, directed=True)
    kind = ga.layout_kind()

    def d_auto(pg, bx):
        pg.add(ga.render(ga.auto_layout(), box=bx, node_radius=6,
                         node_fill=ACCENT_MID, label_size=6.5))

    bk.gallery([
        ("circular_layout — ring of 6", d_circ),
        ("grid_layout — cols=3", d_grid),
        ("layered_layout — a DAG, L-to-R", d_layer),
        ("radial_layout — BFS rings", d_radial),
        ("spring_layout — deterministic FR", d_spring),
        (f"auto_layout — layout_kind()={kind}", d_auto),
    ], cell_h=112)
    bk.verify("the DAG cell must run left-to-right with every arrowhead on "
              "the destination side, and the spring cell must be identical "
              "on every run — the force layout is seeded, so any diff "
              "between two renders is a regression, not noise.")


