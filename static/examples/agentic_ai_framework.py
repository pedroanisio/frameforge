# ------------------------------------------------- ring geometry helpers
def ray_hit(cxo, ro, cxi, ri, deg):
    """Along the ray from the OUTER circle's centre at `deg`, return (ux, uy,
    t_inner) where t_inner is the distance to the inner circle's boundary."""
    th = math.radians(deg)
    ux, uy = math.cos(th), math.sin(th)
    dx = cxi - cxo
    b = ux * dx
    disc = b * b - (dx * dx - ri * ri)
    t_in = b + math.sqrt(max(disc, 0.0))
    return ux, uy, t_in


def ring_mid(outer, inner, deg):
    cxo, ro = LAYERS[outer][0], LAYERS[outer][1]
    cxi, ri = LAYERS[inner][0], LAYERS[inner][1]
    ux, uy, t_in = ray_hit(cxo, ro, cxi, ri, deg)
    t = (t_in + ro) / 2.0
    return cxo + ux * t, CY + uy * t


