def arc_label(outer, inner, center_deg, s, size=10.5):
    from frameforge.sdk import measure_text
    from frameforge.sdk.pathtext import text_on_path
    cxo, ro = LAYERS[outer][0], LAYERS[outer][1]
    cxi, ri = LAYERS[inner][0], LAYERS[inner][1]
    _ux, _uy, t_in = ray_hit(cxo, ro, cxi, ri, center_deg)
    R = (t_in + ro) / 2.0
    length = measure_text(s, font_family="Inter", font_size=size) + 2.5 * len(s)
    half = math.degrees((length / 2.0 + 6.0) / R)
    if center_deg < 0:      # upper seam: walk anticlockwise so glyphs read L→R
        degs = [center_deg - half + k * (2 * half) / 24.0 for k in range(25)]
    else:                   # lower seam: walk clockwise for the same reason
        degs = [center_deg + half - k * (2 * half) / 24.0 for k in range(25)]
    pts = [[cxo + R * math.cos(math.radians(a)), CY + R * math.sin(math.radians(a))]
           for a in degs]
    L.extend(text_on_path(pts, s, size=size, family="Inter", weight=500,
                          color=ITEM))


